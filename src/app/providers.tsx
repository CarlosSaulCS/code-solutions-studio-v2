interface ProvidersProps {
  children: ReactNode
}

export default function Providers({ children }: ProvidersProps) {
  const [currency, setCurrency] = useState<'MXN' | 'USD'>('MXN')
  const [exchangeRate, setExchangeRate] = useState(17.5) // Default MXN to USD rate
  const [language, setLanguage] = useState<'es' | 'en'>('es')
  const [isMounted, setIsMounted] = useState(false)

  // Handle hydration
  useEffect(() => {
    setIsMounted(true)
  }, [])

  // Fetch exchange rate
  useEffect(() => {
    const fetchExchangeRate = async () => {
      try {
        // You can replace this with a real API like exchangerate-api.com
        const response = await fetch('https://api.exchangerate-api.com/v4/latest/USD')
        const data = await response.json()
        setExchangeRate(data.rates.MXN || 17.5)
      } catch (error) {
        console.error('Error fetching exchange rate:', error)
        // Keep default rate if API fails
      }
    }

    if (isMounted) {
      fetchExchangeRate()
    }
  }, [isMounted])

  const convertPrice = (price: number): number => {
    if (currency === 'USD') {
      return price / exchangeRate
    }
    return price
  }
  const t = (key: string, variables?: Record<string, string>): string => {
    const currentTranslations = translations[language]
    
    if (!currentTranslations) {
      console.warn(`Language '${language}' not found, falling back to key`)
      return key
    }

    let translation = currentTranslations[key as keyof typeof currentTranslations]
    
    if (!translation) {
      console.warn(`Translation key '${key}' not found for language '${language}'`)
      // Try to provide a fallback for common patterns
      if (key.includes('.title')) {
        return key.split('.').pop()?.replace(/([A-Z])/g, ' $1').trim() || key
      }
      return key
    }

    // Handle variable interpolation
    if (variables && typeof translation === 'string') {
      Object.keys(variables).forEach(varKey => {
        translation = (translation as string).replace(`{${varKey}}`, variables[varKey])
      })
    }
    
    return translation as string
  }

  return (
    <SessionProvider>
      <CurrencyContext.Provider
        value={{
          currency,
          exchangeRate,
          setCurrency,
          convertPrice,
        }}
      >
        <LanguageContext.Provider
          value={{
            language,
            setLanguage,
            t,
          }}
        >
          {children}
        </LanguageContext.Provider>
      </CurrencyContext.Provider>
    </SessionProvider>
  )
}
